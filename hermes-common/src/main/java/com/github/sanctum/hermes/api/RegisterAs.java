package com.github.sanctum.hermes.api;

import org.jetbrains.annotations.NotNull;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Mark a MessageProvider class for keyed registration upon instantiation.
 *
 * @since 1.0.0
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface RegisterAs {
    /**
     * Get the registration key for this class.
     *
     * @return the registration key for this class
     */
    @NotNull String value();
}
